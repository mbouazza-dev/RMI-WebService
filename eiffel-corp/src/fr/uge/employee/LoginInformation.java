package fr.uge.employee;


public class LoginInformation {
	private int id;
	private String password;
	
	public int getId() {
		return id;
	}
	
	public String getPassword() {
		return password;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public void setPassword(String password) {
		this.password = password;
	}
}
